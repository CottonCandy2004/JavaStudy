package Homework2.Problem2;

public class DigitToString {
    public static void main(String[] args) {
        String a = "1" + 2 + 3 + 4;
        String b = 1 + 2 + 3 + "4";
        String c = "1" + (2 + 3 + 4);
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
    }
}
